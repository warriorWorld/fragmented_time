public class NewsListBean extends BaseBean {
    private String title;
    private String content_text;
    private String content_img;
    private String content_gif;
    private String content_video;
    private String user_head;
    private String user_name;
    private String time;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent_text() {
        return content_text;
    }

    public void setContent_text(String content_text) {
        this.content_text = content_text;
    }

    public String getContent_img() {
        return content_img;
    }

    public void setContent_img(String content_img) {
        this.content_img = content_img;
    }

    public String getContent_gif() {
        return content_gif;
    }

    public void setContent_gif(String content_gif) {
        this.content_gif = content_gif;
    }

    public String getContent_video() {
        return content_video;
    }

    public void setContent_video(String content_video) {
        this.content_video = content_video;
    }

    public String getUser_head() {
        return user_head;
    }

    public void setUser_head(String user_head) {
        this.user_head = user_head;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
